import mongoose from "mongoose";
const { Schema } = mongoose;

const userSchema = new Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    img: {
      type: String,
      required: false,
    },
    country: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: false,
    },
    desc: {
      type: String,
      required: false,
    },
    isSeller: {
      type: Boolean,
      default: false,
    },
    otp: {
      type: String, // Store OTP as a string
    },
    otpExpiry: {
      type: Number, // Store expiry as a timestamp (e.g., Date.now())
    },
  },
  {
    timestamps: true,
  }
);

export default mongoose.model("User", userSchema);
